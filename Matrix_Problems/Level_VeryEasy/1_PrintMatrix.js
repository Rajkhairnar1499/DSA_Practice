function printMatrix(matrix) {
    for (let i = 0; i < matrix.length; i++) {
      for (let j = 0; j < matrix.length; j++) {
        console.log(matrix[i][j]);
      }
    }
  }
  
  //Example Usage
let matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];
printMatrix(matrix);


  